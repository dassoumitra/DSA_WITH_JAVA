public class RelationalOperator {

  public static void main(String[] args) {
    double num1 = 55.2;
    double num2 = 78.21;

    System.out.println(num1+" > "+num2+" : "+(num1>num2));
    System.out.println(num1+" < "+num2+" : "+(num1<num2));
    System.out.println(num1+" >= "+num2+" : "+(num1>=num2));
    System.out.println(num1+" <= "+num2+" : "+(num1<=num2));
    System.out.println(num1+" != "+num2+" : "+(num1!=num2));

  }
}