public class PositiveNegativeZero {
  public static void main(String[] args) {
    int num = -55;

    if(num>0){
      System.out.println(num+" is Positive.");
    }
    else if(num<0){
      System.out.println(num+" is negative.");
    }
    else{
      System.out.println(num+" is zero.");
    }
  }
}
